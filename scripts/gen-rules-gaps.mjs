// Collects every `// RULES-GAP:` comment in engine source into RULES-GAPS.md
// at the repo root, so the designers see open rules questions in one place.
// Run: npm run gen:rules-gaps

import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const srcRoots = [join(root, 'packages')];

function* walk(dir) {
  for (const name of readdirSync(dir)) {
    if (name === 'node_modules') continue;
    const p = join(dir, name);
    if (statSync(p).isDirectory()) yield* walk(p);
    else if (p.endsWith('.ts') || p.endsWith('.mts')) yield p;
  }
}

const gaps = [];
for (const srcRoot of srcRoots) {
  for (const file of walk(srcRoot)) {
    const lines = readFileSync(file, 'utf8').split('\n');
    for (let i = 0; i < lines.length; i++) {
      const m = lines[i].match(/(?:\/\/|\*)\s*RULES-GAP:\s*(.*)/);
      if (!m) continue;
      // capture continuation comment lines until the comment block ends
      let text = m[1].trim();
      let j = i + 1;
      while (j < lines.length) {
        const cont = lines[j].match(/^\s*(?:\/\/|\*)\s?(?!RULES-GAP)(.*)/);
        if (!cont || cont[1].trim() === '' || /^[-=*/]+$/.test(cont[1].trim())) break;
        text += ' ' + cont[1].trim();
        j++;
      }
      gaps.push({ file: relative(root, file), line: i + 1, text });
    }
  }
}

const header = `# RULES-GAPS

Open rules questions surfaced during implementation. Each of these is a spot
where the design docs are silent; the engine implements the most conservative
interpretation (behind a RulesConfig flag where applicable) and tags the code
with \`// RULES-GAP:\`. **Decide these with the designers, then delete the tag.**

Generated by \`npm run gen:rules-gaps\` — do not edit by hand.

`;

const body = gaps
  .map((g, i) => `${i + 1}. **\`${g.file}:${g.line}\`** — ${g.text}`)
  .join('\n');

writeFileSync(join(root, 'RULES-GAPS.md'), header + body + '\n');
console.log(`RULES-GAPS.md: ${gaps.length} gap(s) recorded.`);
